namespace GAP.Helper
{
    public class Desc
    {
        private string nom;
        private int nombrePiece;
        private string desc;


        public string Nom
        {
            get { return nom; }
            set { nom = value; }
        }
        public int NombrePiece
        {
            get { return nombrePiece; }
            set { nombrePiece = value; }
        }

        public string Description
        {
            get { return desc; }
            set { desc = value; }
        }
    }

}
